/**
 * The `Strategy` enum represents the different strategies a player or character can
 * use in a game or simulation.
 */
package un.caen.util;

/**
 * The `Strategy` enum defines the following two strategies:
 * <ul>
 *   <li>`MOVE`</li>
 *   <li>`ATTACK`</li>
 * </ul>
 * These strategies can be used to represent the different ways a player or character
 * can approach a game or simulation.
 */
public enum Strategy {
    /**
     * Represents a strategy focused on moving the player or character.
     */
    MOVE,
    /**
     * Represents a strategy focused on attacking other players or characters.
     */
    ATTACK
}
